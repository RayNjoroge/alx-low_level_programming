/**
 * _abs - entry point
 * @numeral: - parameter of type int
 * Return: - returns desired value
 * if ... else statement - to set the conditions to be checked
 */
int _abs(int numeral)
{
	if (numeral < 0)
	{
		return (-numeral);
	}
	else
	{
		return (numeral);
	}
}
