/**
 * _isupper - entry point
 * @c: - parameter of type int
 * if...else statement to set the conditions to check for
 * Return: - output 1 or 0 depending on the conditions checked above
 */
int _isupper(int c)
{
	if (c == 'A')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
